package com.techaxis.foodInfo;

public class Energy {
	public Energy() {
		
	}
	
	 String menuItems;
	 double energyTotal;
	 double protenTotal;
	 double fiberTotal;
	 double carbohydrate;
	double fatTotal;
	/**
	 * @return the menuItems
	 */
	public String getMenuItems() {
		return menuItems;
	}
	/**
	 * @param menuItems the menuItems to set
	 */
	public void setMenuItems(String menuItems) {
		this.menuItems = menuItems;
	}
	/**
	 * @return the energyTotal
	 */
	public double getEnergyTotal() {
		return energyTotal;
	}
	/**
	 * @param energyTotal the energyTotal to set
	 */
	public void setEnergyTotal(double energyTotal) {
		this.energyTotal = energyTotal;
	}
	/**
	 * @return the protenTotal
	 */
	public double getProtenTotal() {
		return protenTotal;
	}
	/**
	 * @param protenTotal the protenTotal to set
	 */
	public void setProtenTotal(double protenTotal) {
		this.protenTotal = protenTotal;
	}
	/**
	 * @return the fiberTotal
	 */
	public double getFiberTotal() {
		return fiberTotal;
	}
	/**
	 * @param fiberTotal the fiberTotal to set
	 */
	public void setFiberTotal(double fiberTotal) {
		this.fiberTotal = fiberTotal;
	}
	/**
	 * @return the carbohydrate
	 */
	public double getCarbohydrate() {
		return carbohydrate;
	}
	/**
	 * @param carbohydrate the carbohydrate to set
	 */
	public void setCarbohydrate(double carbohydrate) {
		this.carbohydrate = carbohydrate;
	}
	/**
	 * @return the fatTotal
	 */
	public double getFatTotal() {
		return fatTotal;
	}
	/**
	 * @param fatTotal the fatTotal to set
	 */
	public void setFatTotal(double fatTotal) {
		this.fatTotal = fatTotal;
	}

}
